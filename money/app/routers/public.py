@router.get("/", response_class=HTMLResponse)
def homepage(
    request: Request,
    db: Session = Depends(dependencies.get_db_dep),
    settings: Settings = Depends(dependencies.get_runtime_settings),
):
    latest_pages = (
        db.query(Page)
        .filter(Page.status == PageStatus.PUBLISHED)
        .order_by(Page.published_at.desc())
        .limit(8)
        .all()
    )
    featured = (
        db.query(Page)
        .filter(Page.featured.is_(True))
        .order_by(Page.updated_at.desc())
        .limit(4)
        .all()
    )
    categories = db.query(Category).all()
    context = {
        "request": request,
        "pages": latest_pages,
        "featured": featured or latest_pages[:3],
        "categories": categories,
        "meta": metadata.default_meta(),
        "settings": settings,
    }
    return templates.TemplateResponse("public/home.html", context)


@router.get("/category/{slug}", response_class=HTMLResponse)
def category_page(
    slug: str,
    request: Request,
    db: Session = Depends(dependencies.get_db_dep),
    settings: Settings = Depends(dependencies.get_runtime_settings),
):
    category = db.query(Category).filter(Category.slug == slug).first()
    if not category:
        raise HTTPException(status_code=404)
    pages = (
        db.query(Page)
        .filter(Page.category_id == category.id, Page.status == PageStatus.PUBLISHED)
        .order_by(Page.published_at.desc())
        .all()
    )
    return templates.TemplateResponse(
        "public/category.html",
        {
            "request": request,
            "category": category,
            "pages": pages,
            "meta": metadata.default_meta(),
            "settings": settings,
        },
    )


@router.get("/search", response_class=HTMLResponse)
def search(
    request: Request,
    q: str = Query(""),
    db: Session = Depends(dependencies.get_db_dep),
    settings: Settings = Depends(dependencies.get_runtime_settings),
):
    if q:
        client_host = request.client.host if request.client else "anonymous"
        allowed = check_rate_limit(
            identifier=f"search:{client_host}",
            limit=settings.search_rate_limit_per_minute,
        )
        if not allowed:
            raise HTTPException(status_code=429, detail="Too many search requests")
    query = db.query(Page).filter(Page.status == PageStatus.PUBLISHED)
    if q:
        query = query.filter(Page.title.ilike(f"%{q}%"))
    results = query.order_by(Page.published_at.desc()).limit(20).all()
    return templates.TemplateResponse(
        "public/search.html",
        {
            "request": request,
            "query": q,
            "results": results,
            "meta": metadata.default_meta(),
            "settings": settings,
        },
    )


