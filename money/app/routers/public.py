@router.get("/sitemap.xml")
def sitemap(db: Session = Depends(dependencies.get_db_dep)):
    xml = latest_sitemap()
    if not xml:
        from app.seo.sitemap import build_sitemap

        xml = build_sitemap(db)
    return Response(content=xml, media_type="application/xml")


@router.get("/rss.xml")
def rss_feed(
    db: Session = Depends(dependencies.get_db_dep),
    settings: Settings = Depends(dependencies.get_runtime_settings),
):
    items = (
        db.query(Page)
        .filter(Page.status == PageStatus.PUBLISHED)
        .order_by(Page.published_at.desc())
        .limit(15)
        .all()
    )
    rss_items = ""
    for page in items:
        link = f"{settings.site_url}/{page.slug}"
        pub_date = page.published_at.isoformat() if page.published_at else ""
        rss_items += f"<item><title>{page.title}</title><link>{link}</link><pubDate>{pub_date}</pubDate></item>"
    xml = (
        "<?xml version=\"1.0\" encoding=\"UTF-8\"?>"
        "<rss version=\"2.0\"><channel><title>Auto Publishing Feed</title>"
        f"{rss_items}</channel></rss>"
    )
    return Response(content=xml, media_type="application/xml")


