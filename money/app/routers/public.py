@router.get("/{slug}", response_class=HTMLResponse)
def page_detail(
    slug: str,
    request: Request,
    db: Session = Depends(dependencies.get_db_dep),
    settings: Settings = Depends(dependencies.get_runtime_settings),
):
    page = db.query(Page).filter(Page.slug == slug, Page.status == PageStatus.PUBLISHED).first()
    if not page:
        raise HTTPException(status_code=404)
    related = (
        db.query(Page)
        .filter(Page.id != page.id, Page.category_id == page.category_id, Page.status == PageStatus.PUBLISHED)
        .order_by(Page.published_at.desc())
        .limit(6)
        .all()
    )
    return templates.TemplateResponse(
        "public/page_detail.html",
        {
            "request": request,
            "page": page,
            "related": related,
            "meta": metadata.page_meta(page),
            "settings": settings,
        },
    )


